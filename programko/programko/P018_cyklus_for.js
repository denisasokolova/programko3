/*
for (inicializce -kus kodu krery se ma provest pri funkci for; podminka; iterace){
    ...kod co se opakuje
}
 */
for (let i = 0; i < 10;  i++) { // prvni let=1, druhy i<10, treti console.log(i), ctvrty i++ (hodnota se zvysuje o 1)
    console.log(i);
    
}

//do zavorky uvadime veskery kod ktery se tyka stanoveni poctu opakovani 

/*nekonmecny cyklus for*/
for (let y = 0; y => 0;  y++) {
    console.log(y);
    
}